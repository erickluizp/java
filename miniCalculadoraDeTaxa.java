public class miniCalculadoraDeTaxa {
    public static void main(String[] args) {
        double salarioAnual = 45000;
        double primeriaTaxa = 9.70 / 100;
        double segundaTaxa = 37.35 / 100;
        double terceiraTaxa = 49.50 / 100;
        double valorImposto;
        if(salarioAnual <= 34712){
            valorImposto = salarioAnual * primeriaTaxa;
        }else if(salarioAnual >= 34713 && salarioAnual <= 68507) {
            valorImposto = salarioAnual * segundaTaxa;
        }else {
            valorImposto = salarioAnual * terceiraTaxa;
        }
        System.out.println(valorImposto);
    }
}
